package Day_11.Home_Task;

public class SeatingArrangements {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("R" + i + "S" + j + " ");
            }
            System.out.println();
        }
    }
}
